// Вспомогательная функция: RA (часы) + Dec (градусы) → x,y в viewBox 160×240
// RA  0–24h → x: 22..138  (правее = меньше RA, как на звёздных картах)
// Dec -90..+90 → y: 22..218
function radec(ra, dec) {
  const x = Math.round(138 - (ra / 24) * 116);
  const y = Math.round(22 + ((90 - dec) / 180) * 196);
  return { x, y };
}

// Строим точки созвездия из списка [ra, dec] пар
function pts(coords) {
  return coords.map(([ra, dec]) => ({ ...radec(ra, dec), r: 2.8 }));
}

